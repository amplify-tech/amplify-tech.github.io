#include<iostream>
using namespace std;

int main(){
  int x;
  cout<<"Enter the value of x (integer) ";
  cin>>x;
  cout<<"the value of x is "<<x<<endl;

  char y;
  cout<<"Enter the value of y (a character) ";
  cin>>y;
  cout<<"the value of y is "<<y<<endl;

  bool z;
  cout<<"Enter the value of z (a bool variable) ";
  cin>>z;
  cout<<"the value of z is "<<z<<endl;
}
